package ru.kpfu.itis.shkalin.simplifytorrent.protocol.message.exception;

public class MessageException extends Exception {
    public MessageException() {
        super("unknown message exception");
    }

    public MessageException(String message) {
        super(message);
    }

    public MessageException(String message, Throwable cause) {
        super(message, cause);
    }

    public MessageException(Throwable cause) {
        super(cause);
    }

    public MessageException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
